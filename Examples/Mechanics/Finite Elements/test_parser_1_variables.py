# TEST 1: Variables simples (Python)
# ====================================

# PRUEBA 1A: Variables simples
a = 6
b = 4
t = 0.1
print('PRUEBA 1A:')
print(f'a = {a}')
print(f'b = {b}')
print(f't = {t}')

# PRUEBA 1B: Variables (sin unidades en Python)
E = 35000e6  # Pa
q = 10       # kN/m2
print('\nPRUEBA 1B:')
print(f'E = {E} Pa')
print(f'q = {q} kN/m2')

# PRUEBA 1C: Variable n
n = 16
print('\nPRUEBA 1C:')
print(f'n = {n}')

# PRUEBA 1D: Expresiones
c = a + b
d = a * b
e = a / b
print('\nPRUEBA 1D:')
print(f'c = a + b = {c}')
print(f'd = a * b = {d}')
print(f'e = a / b = {e}')
